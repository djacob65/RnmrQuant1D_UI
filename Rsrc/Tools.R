	close(fh)
}


#----
# Copy a folder content (source) to another folder (destination)
#----
copy_folder <- function(source, destination)
{
	if (!dir.exists(source))
		stop("The folder does not exist : ", source)

	# Create the destination folder if it does not exist.
	if (!dir.exists(destination))
		dir.create(destination, recursive = TRUE)

	# List all files and subfolders of the source
	lstfiles <- list.files(source, full.names = TRUE, recursive = FALSE)

	# Copy each item to the destination root (avoids nesting)
	out <- file.copy(from = lstfiles, to = destination, recursive = TRUE, overwrite = TRUE)
}


