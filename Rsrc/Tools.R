#----
# Executes a method from the RnmrQuant1D package, intercepting —where applicable— 
# the message and interruption generated by the internal `stop_quietly` method.
#----
exe.catch <- function(expr)
{
	message_text <- NULL
	result <- tryCatch({
		withCallingHandlers({
			expr
		}, message = function(m) { # intercept the message
			message_text <<- conditionMessage(m)
			invokeRestart("muffleMessage")  # prevents the message from appearing in the R console
		})
	}, interrupt = function(cnd) { # intercept the interrupt
		NULL
	})
	list(
		result = result,
		message = message_text,
		error_occurred = is.null(result) && !is.null(message_text)
	)
}

#----
# Check if the content of the sample metadata file is compliant with the operation type
#----
check_samples_metadata <- function(rq1d, samples, sdir, vendor, onlyintg)
{
	switch( vendor,
		'bruker' = { colstr <- c("Spectrum", "Samplename", "EXPNO") },
		'varian' = { colstr <- c("Spectrum", "Samplename") },
		'jeol'   = { colstr <- c("Spectrum", "Samplename") }
	)

	ret <- ''
	repeat {
		# Get sample list
		priv <- rq1d$.__enclos_env__$private
		df <- tryCatch({
			priv$get_list_spectrum(sdir, priv$get_list_samples(sdir))
		}, error=function(cond) {
			ret <- paste(cond, collapse="\n")
		})
		if (nchar(ret)) break

		# Check if the samples colnames include the rigth ones
		if (sum(colstr %in% colnames(samples))<length(colstr)) {
			ret <- paste0("Error: the sample colnames must include at least the columns : ",
				paste(colstr,collapse=", "))
			break
		}

		# Samplecodes
		if (vendor=='bruker') {
			samplecode <- paste(samples[,1], samples[,3], sep = "-")
		} else {
			samplecode <- paste(samples[,1],1, sep="-")
		}

		if (!onlyintg) {
			# Check if the samples table has a 'F_dilition' columns
			if (! "F_dilution" %in% colnames(samples)) {
				ret <- "Error: the sample table must have a columns named 'F_dilution'"
				break
			}
			# Check if the samples table has a 'Type' columns
			if (! "Type" %in% colnames(samples)) {
				ret <- "Error: the sample table must have a columns named 'Type'"
				break
			}
			# Check if values of the 'TYPE' columns are valid ones 
			if (sum(! samples$Type %in% sampleTypes)>0) {
				ret <- paste0("Error: Valid values for 'Type' columns are limited to ",
					paste(sampleTypes, collapse=', '))
				break
			}
		}

		# Check whether all spectrum names correspond to a spectrum directory.
		if( sum(samplecode %in% df[,2]) < length(samplecode) ) {
			ret <- paste0("Error: Some spectrum names do not correspond to a spectrum directory:\n",
					paste(samples[ ! samplecode %in% df[,2], 1], collapse=', '))
			break
		}

		# Check whether all spectra have the same frequence
		df <- df[df[,2] %in% samplecode, c(1:4,6:7)]
		freq <- unique(round(as.numeric(df[,5])))
		if ( length(freq)>1 ) {
			ret <- "Error: All spectrum must have the same frequence\n"
			break
		}

		# Check whether all spectra have the same size
		size <- unique(round(as.numeric(df[,6])))
		if ( length(size)>1 ) {
			ret <- "Error: All spectrum must have the same size\n"
			break
		}
		break
	}
	ret
}


#----
# Get the template of sample metadata depending on the operation type
#----
get_samples_metadata <- function(sdir, vendor, onlyintg)
{
	switch( vendor,
		'bruker' = { colstr <- c("Spectrum", "Samplename", "EXPNO") },
		'varian' = { colstr <- c("Spectrum", "Samplename") },
		'jeol'   = { colstr <- c("Spectrum", "Samplename") }
	)

	rq1d <- RnmrQuant1D$new(vendor)
	priv <- rq1d$.__enclos_env__$private

	samples <- priv$get_list_spectrum(sdir, priv$get_list_samples(sdir))
	if (!onlyintg) {
		samples <- cbind( samples[,1:length(colstr)], rep(sampleTypes[1], nrow(samples)), rep(fac_dilution, nrow(samples)) )
		colnames(samples) <- c( colstr, 'Type', rq1d$FDILfield)
		samples$Type[grep(QCQS[1], samples[,1])] <- QCQS[1]
		samples$Type[grep(QCQS[2], samples[,1])] <- QCQS[2]
	} else {
		samples <- samples[,1:length(colstr)]
		colnames(samples) <- colstr
	}
	samples 
}

#----
# Check if file has a XLSX format
#----
is_xlsx <- function(file)
{
	con <- file(file, "rb")
	sig <- readBin(con, "raw", n = 4)
	close(con)  
	# Signature ZIP : 50 4B 03 04
	identical(sig, as.raw(c(0x50, 0x4B, 0x03, 0x04)))
}

#----
# Open the sample file, regardless of its format (TSV or XLSX).
#----
open_samples_file <- function(samplefile)
{
	out <- tryCatch({
		if (is_xlsx(samplefile)) {
			openxlsx::read.xlsx(samplefile, sheet=1)
		} else {
			as.data.frame(utils::read.table(samplefile, sep="\t", header=T,stringsAsFactors=FALSE))
		}
	}, error=function(cond) {
		out <- paste(cond, collapse="\n")
	})
	return(out)
}

